
marks = float(input("please given the number: "))

if(marks>=80):
    print("Result is: A+")
elif (marks >= 70):
    print("Result is: A")
elif (marks >= 60):
    print("Result is: A-")
elif (marks >= 50):
    print("Result is: B")
elif (marks >= 40):
    print("Result is: C")
elif (marks >= 33):
    print("Result is: D")
else:
    print("Result is fail")